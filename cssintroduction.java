// lets talk about css which means cascading style sheets
// it used to colour or style our html web page
// to run it we must know about selector,property,value
// selector means we want to select some line of code what you want to colour or style
// property means which colour you want to apply or which style you want to apply
// 3 ways of css implementation
// 1) inline style sheets: sytle applied to one line or one tag
// 2) internal style sheets: sytle applied to one entire web page
// 3) external style sheets:sytle applied to one or more web pages
